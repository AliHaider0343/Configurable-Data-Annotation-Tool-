    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (model.getRowCount() <= 0)
            JOptionPane.showMessageDialog(this, "Please Select at least No Term or single Opinion Term", "Information", JOptionPane.ERROR_MESSAGE);
        else {
            if (textReturnedListener != null) {
                textReturnedListener.onTextReturned(readTableData(), this.checkboxName); // Notify the listener
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed
    private String readTableData() {

        int rowCount = model.getRowCount();

        int columnCount = model.getColumnCount();
        String data = "[";
        for (int row = 0; row < rowCount; row++) {
            for (int column = 0; column < columnCount; column++) {
                data += model.getValueAt(row, column);
                if (column < columnCount - 1) {
                    data += "-";
                }
            }
            if (row < rowCount - 1) {
                data += ",";
            }

        }
        data += "]";
        return data;
    }

    public Pair<Integer, Integer> findSubstringIndices(String sentence, String substring) {
        int startIndex = sentence.indexOf(substring);
        if (startIndex != -1) {
            int endIndex = startIndex + substring.length() - 1;
            return new Pair<>(startIndex, endIndex);
        }

        return null; // Substring not found
    }

    private void removeLastRow() {
        int rowCount = model.getRowCount();

        if (rowCount > 0) {
            model.removeRow(rowCount - 1);
        } else {
            jButton3.setEnabled(true);
            jButton1.setEnabled(true);
        }
    }

    // Pair class definition
    static class Pair<A, B> {

        private final A first;
        private final B second;

        public Pair(A first, B second) {
            this.first = first;
            this.second = second;
        }

        public A getFirst() {
            return first;
        }

        public B getSecond() {
            return second;
        }
    }

